package Kalkulacka;

public class Kalkulacka {
    double cislo1;
    double cislo2;

    public double scitani(double c1, double c2){
        return c1+c2;
    }
    public double odcitani(double c1, double c2){
        return c1-c2;
    }
    public double nasobeni(double c1, double c2){
        return c1*c2;
    }
    public double deleni(double c1, double c2){
        return c1/c2;
    }

}
